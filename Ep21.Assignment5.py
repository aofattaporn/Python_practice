# แปลง พ.ศ. เป็น ค.ศ.
print("ต้องการเปลี่ยน ค.ศ.เป็น พ.ศ. กด 1 ")
print("ต้องการเปลี่ยน พ.ศ.เป็น ค.ศ. กด 2 ")
x = int(input("กรุณาเลือก : "))
if x==1 or x==2 :
    if x == 1 :
        year = int(input("ป้อนค.ศ. : "))
        print("ค.ศ.", year ,"ตรงกับพ.ศ. :", year+543)
    if x == 2 :
        year = int(input("ป้อนพ.ศ. : "))
        print("พ.ศ.", year ,"ตรงกับค.ศ. :", year-543)      
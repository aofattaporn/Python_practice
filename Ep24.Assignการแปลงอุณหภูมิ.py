# การแปลงอุณหูมิ
# ป้อนอุณหภูมิ ป้อนหน่วยมาด้วย 
# แปลงเป็นหน่วยที่ต้องการ
temp = input("ป้อนอุณหภูมิ (องศา) :")

degree = int(temp[:-1])    # เอาแค่ตัวแรกถึงก่อนตัวท้าย 
unit = temp[-1].upper()    # เอาแค่ตัวท้าย + ทำให้เป็นตัวพิมพ์ใหญ่

if unit=="C" :
    result=(degree*9)/5+32
    unit_result="ฟาเรนไฮน์" # แปลงให้เป็นหาเรนไฮน์ 
if unit=="F" :
    result=(degree-32)*5/9
    unit_result="เซลเซียส"  # แปลงให้เป็งองศาเซลเซียส

print("แปลงตัวเลข = ",temp,"เป็น",unit_result, "=",result)